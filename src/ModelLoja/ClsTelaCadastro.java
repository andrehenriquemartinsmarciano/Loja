package ModelLoja;

public class ClsTelaCadastro {

    private String Nome;
    private Float cpf;
    private String email;
    private Float senha;

    public String getNome() {
        return Nome;
    }

    public void setNome(String Nome) {
        this.Nome = Nome;
    }

    public Float getCpf() {
        return cpf;
    }

    public void setCpf(Float cpf) {
        this.cpf = cpf;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Float getSenha() {
        return senha;
    }

    public void setSenha(Float senha) {
        this.senha = senha;
    }
    
    
}
